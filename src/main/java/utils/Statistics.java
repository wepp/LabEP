package utils;

public class Statistics {
    private double nseeds;
    private double np;
    private double gp;
    private double lp;
    private double pr;
    private double gpr;
    private double lpr;
    private double fpr;

    public double getNseeds() {
        return nseeds;
    }

    public void setNseeds(double nseeds) {
        this.nseeds = nseeds;
    }

    public double getNp() {
        return np;
    }

    public void setNp(double np) {
        this.np = np;
    }

    public double getGp() {
        return gp;
    }

    public void setGp(double gp) {
        this.gp = gp;
    }

    public double getLp() {
        return lp;
    }

    public void setLp(double lp) {
        this.lp = lp;
    }

    public double getPr() {
        return pr;
    }

    public void setPr(double pr) {
        this.pr = pr;
    }

    public double getGpr() {
        return gpr;
    }

    public void setGpr(double gpr) {
        this.gpr = gpr;
    }

    public double getLpr() {
        return lpr;
    }

    public void setLpr(double lpr) {
        this.lpr = lpr;
    }

    public double getFpr() {
        return fpr;
    }

    public void setFpr(double fpr) {
        this.fpr = fpr;
    }
}
